# 7. Cálculo de Velocidade Média:
# • Receba a distância percorrida e o tempo gasto.
# • Calcule e exiba a velocidade média.

distancia_percorrida = float(input("Digite a distância percorrida:"))
tempo_gasto = float(input("Digite o tempo gasto:"))
velocidade_media = distancia_percorrida / tempo_gasto
print("A velocidade média é: ", velocidade_media) 